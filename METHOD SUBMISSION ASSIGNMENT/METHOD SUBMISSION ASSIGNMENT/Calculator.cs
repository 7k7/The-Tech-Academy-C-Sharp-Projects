
namespace METHOD_SUBMISSION_ASSIGNMENT
{
    // This class represents a calculator for performing
    // mathematical operations on numbers
    internal class Calculator
    {
        // f() takes in two integers, with one optional parameter
        // using a default value which is used if not argument is passed in
        public int Add(int num1, int num2 = 1)
        {
            return num1 + num2;
        }
    }
}
